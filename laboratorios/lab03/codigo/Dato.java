public class Dato 
{

    String nombre_;
    int codigo;
    String codMateria;
    String semestre;
    int grupo;  
    String evaluacion;
    double porcentaje;
    String descripcion;
    String materia;
    int nota;
    int definitiva;

    public Dato(String nombre_, int codigo, String codMateria, String semestre, int grupo, String evaluacion, double porcentaje, String descripcion, String materia, int nota, int definitiva) {

        this.nombre_ = nombre_;
        this.codigo = codigo;
        this.codMateria = codMateria;
        this.semestre = semestre;
        this.grupo = grupo;
        this.evaluacion = evaluacion;
        this.porcentaje = porcentaje;
        this.descripcion = descripcion;
        this.materia = materia;
        this.nota = nota;
        this.definitiva = definitiva;
    }

}